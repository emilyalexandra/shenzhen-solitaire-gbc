void deal(){
	#define card_place_x k
	#define card_place_y l
	#define sprite_x a
	#define sprite_y b
	#define dest_x c
	#define dest_y d
	//Shuffle the deck
	initrand(random_seed);
	for(i = 39; i > 0; i--){
		j = rand();
		j = j % (i + 1);
		if(i != j){
			deck[i] = deck[i] + deck[j];//Kinda gross
			deck[j] = deck[i] - deck[j];
			deck[i] = deck[i] - deck[j];
		}
	}
	card_place_x = 0;//Current horizontal placement position
	card_place_y = 0;//Current vertical placement position
	sprite_layer_count = 3;
	for(i = 0; i < 40; i++){
		set_stacks(card_place_x, card_place_y, deck[i]);
		sprite_x = 80;
		sprite_y = 24;
		dest_x = 24 + ((INT16)card_place_x << 4);
		dest_y = 56 + ((INT16)card_place_y << 3);
		move_card(deck[i]);
		if(i > 7){
			current_card[0] += 104;
			current_card[1] = 106;
		}
		VBK_REG = 1;
		set_bkg_tiles(2 + (card_place_x << 1), 5 + card_place_y, 2, 3, current_card_att);
		VBK_REG = 0;
		set_bkg_tiles(2 + (card_place_x << 1), 5 + card_place_y, 2, 3, current_card);
		wait_vbl_done();
		card_place_x++;
		if(card_place_x > 7){
			card_place_x = 0;
			card_place_y++;
		}
	}
	move_sprite(0, 0, 0);
	move_sprite(1, 0, 0);
	move_sprite(2, 0, 0);
	move_sprite(3, 0, 0);
	move_sprite(4, 0, 0);
	move_sprite(5, 0, 0);
	#undef card_place_x
	#undef card_place_y
	#undef sprite_x
	#undef sprite_y
	#undef dest_x
	#undef dest_y
}
