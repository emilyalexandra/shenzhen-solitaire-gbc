void move_cursor(){
	if(carry_card_count > 0){
		if(cursor_y < 2){
			get_card(carry_cards[0], 0);
		}else if(cursor_y > 1){
			get_card(carry_cards[0], 1);
		}
		set_sprite_tile(0, current_card[0]);
		set_sprite_tile(1, current_card[1]);
	}
	if(cursor_y == 0){
		if(cursor_x == 3) move_sprites(80, 16);
		else move_sprites(24 + (cursor_x << 4), 24);
	}else{
		move_sprites(24 + (cursor_x << 4), 48 + (cursor_y << 3));
	}
}
void check_automove(){
	#define recheck k
	#define card_check l
	#define card_digit m
	#define blank_count n
	#define current_stack_height o
	#define sprite_x a
	#define sprite_y b
	#define dest_x c
	#define dest_y d
	recheck = 1;
	while(recheck){
		recheck = 0;
		blank_count = 0;
		for(i = 0; i < 8; i++){
			if(stack_heights[i] == 0){
				blank_count++;
				continue;
			}
			card_check = get_stacks(i, stack_heights[i] - 1);
			card_digit = card_check & 15;
			suit = (card_check & 48) >> 4;
			if(card_check == 0x3A){//Rose
				recheck = 1;
				sprite_x = 24 + ((INT16)i << 4);
				p = (stack_heights[i] << 3);//Why are these three lines like this?
				p = p + 48;//Well the compiler is very mean and will not let me write it any different
				sprite_y = (UINT16) p;//Thanks for coming to my ted talk
				dest_x = 80;
				dest_y = 24;
				stack_heights[i]--;
				get_card(get_stacks(i, stack_heights[i] - 1), stack_heights[i] > 1);
				VBK_REG = 1;
				set_bkg_tiles(2 + (i << 1), 7 + stack_heights[i], 2, 1, empty_slot_att);
				if(stack_heights[i] == 0) set_bkg_tiles(2 + (i << 1), 5, 2, 3, empty_slot_att);
				else set_bkg_tiles(2 + (i << 1), 4 + stack_heights[i], 2, 3, current_card_att);
				VBK_REG = 0;
				set_bkg_tiles(2 + (i << 1), 7 + stack_heights[i], 2, 1, empty_slot_att);
				if(stack_heights[i] == 0) set_bkg_tiles(2 + (i << 1), 5, 2, 3, empty_slot_map);
				else set_bkg_tiles(2 + (i << 1), 4 + stack_heights[i], 2, 3, current_card);
				move_card(card_check);
				VBK_REG = 1;
				set_bkg_tiles(9, 1, 2, 3, current_card_att);
				VBK_REG = 0;
				set_bkg_tiles(9, 1, 2, 3, current_card);
			}else if(card_digit != 0x09){
				if(card_digit == suit_stacks[suit]){
					if((card_digit < 0x02) || (suit_stacks[0] >= card_digit && suit_stacks[1] >= card_digit && suit_stacks[2] >= card_digit)){
						recheck = 1;
						sprite_x = 24 + ((INT16)i << 4);
						p = (stack_heights[i] << 3);//Why are these three lines like this?
						p = p + 48;//Well the compiler is very mean and will not let me write it any different
						sprite_y = (UINT16) p;//Thanks for coming to my ted talk
						dest_x = 104 + ((INT16)suit << 4);
						dest_y = 24;
						stack_heights[i] = stack_heights[i] - 1;
						suit_stacks[suit] = card_digit + 1;
						get_card(get_stacks(i, stack_heights[i] - 1), stack_heights[i] > 1);
						VBK_REG = 1;
						set_bkg_tiles(2 + (i << 1), 7 + stack_heights[i], 2, 1, empty_slot_att);
						if(stack_heights[i] == 0) set_bkg_tiles(2 + (i << 1), 5, 2, 3, empty_slot_att);
						else set_bkg_tiles(2 + (i << 1), 4 + stack_heights[i], 2, 3, current_card_att);
						VBK_REG = 0;
						set_bkg_tiles(2 + (i << 1), 7 + stack_heights[i], 2, 1, empty_slot_att);
						if(stack_heights[i] == 0) set_bkg_tiles(2 + (i << 1), 5, 2, 3, empty_slot_map);
						else set_bkg_tiles(2 + (i << 1), 4 + stack_heights[i], 2, 3, current_card);
						move_card(card_check);
						VBK_REG = 1;//You know I only *really* have to set the palette once
						set_bkg_tiles(12 + (suit << 1), 1, 2, 3, current_card_att);
						VBK_REG = 0;
						set_bkg_tiles(12 + (suit << 1), 1, 2, 3, current_card);
					}
				}
			}
		}
		for(i = 0; i < 3; i++){
			if(free_slots[i] >= 0xFE){
				blank_count++;
				continue;
			}
			card_check = free_slots[i];
			card_digit = card_check & 15;
			suit = (card_check & 48) >> 4;
			if(card_digit != 0x09){
				if(card_digit == suit_stacks[suit]){
					if((card_digit < 0x02) || (suit_stacks[0] >= card_digit && suit_stacks[1] >= card_digit && suit_stacks[2] >= card_digit)){
						recheck = 1;
						sprite_x = 24 + ((INT16)i << 4);
						sprite_y = 24;
						dest_x = 104 + ((INT16)suit << 4);
						dest_y = 24;
						suit_stacks[suit]++;
						VBK_REG = 1;
						set_bkg_tiles(2 + (i << 1), 1, 2, 3, empty_slot_att);
						VBK_REG = 0;
						set_bkg_tiles(2 + (i << 1), 1, 2, 3, empty_slot_map);
						move_card(card_check);
						VBK_REG = 1;
						set_bkg_tiles(12 + (suit << 1), 1, 2, 3, current_card_att);
						VBK_REG = 0;
						set_bkg_tiles(12 + (suit << 1), 1, 2, 3, current_card);
						free_slots[i] = 0xFF;
					}
				}
			}
		}
		if(blank_count == 11){//Kinda gross but whatever
			move_sprites(200, 0);
			win_digits[7]++;
			if(win_digits[7] >= 10){
				win_digits[7] = 0;
				win_digits[6]++;
				if(win_digits[6] >= 10){
					win_digits[6] = 0;
					win_digits[5]++;
					if(win_digits[5] >= 10){
						win_digits[5] = 0;
						win_digits[4]++;
						if(win_digits[4] >= 10){
							win_digits[4] = 0;
							win_digits[3]++;
							if(win_digits[3] >= 10){
								win_digits[3] = 0;
								win_digits[2]++;
								if(win_digits[2] >= 10){
									win_digits[2] = 0;
									win_digits[1]++;
									if(win_digits[1] >= 10){
										win_digits[1] = 0;
										win_digits[0]++;
										if(win_digits[0] >= 10){
											win_digits[0] = 9;//I highly doubt anyone will win this game one hundred million times, so this is fine
										}
									}
								}
							}
						}
					}
				}
			}
			save_game();
			update_win_counter();
			game_won = 1;
		}
	}
	if(cursor_y != 0){
		cursor_y = stack_heights[cursor_x];
		if(cursor_y == 0) cursor_y = 1;
	}
	init_cursor();
	#undef recheck
	#undef card_check
	#undef card_digit
	#undef blank_count
	#undef current_stack_height
	#undef sprite_x
	#undef sprite_y
	#undef dest_x
	#undef dest_y
}
UINT8 can_stack(UINT8 a, UINT8 b){
	if((a & 48) != (b & 48) && (a & 15) + 1 == (b & 15) && (b & 15) != 0x09) return 1;
	return 0;
}
